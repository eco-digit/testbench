CREATE TABLE repositoryentity (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    isPublic BOOLEAN NOT NULL,
    project_id bigint NOT NULL,
    repositoryLink VARCHAR(1024) NOT NULL,
    repositoryName VARCHAR(255) NOT NULL,
    -- data for access token
    associatedUser VARCHAR(255),
    name VARCHAR(255),
    token VARCHAR(1024)
);